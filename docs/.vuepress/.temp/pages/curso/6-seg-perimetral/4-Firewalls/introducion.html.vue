<template><div><h1 id="¿que-es" tabindex="-1"><a class="header-anchor" href="#¿que-es" aria-hidden="true">#</a> ¿Que es?</h1>
<p>El firewall es un software especializado que se interpone entre las aplicaciones y el software de red para hacer un filtrado de paquetes:</p>
<ul>
<li>
<p>en el <strong>tráfico entrante</strong>:  la tarjeta de red recibe el paquete y lo identifica, pero antes de entregarlo a la aplicación correspondiente, pasa por el firewall para que decida si prospera o no.</p>
</li>
<li>
<p>en el <strong>tráfico saliente</strong>: las aplicaciones elaboran sus paquetes de datos, pero antes de entregarlo al software de red para que lo envíe, pasa por el firewall.</p>
</li>
</ul>
<ul>
<li>
<p>La inteligencia del firewall se expresa mediante <strong>reglas de configuración</strong>. El administrador de la máquina puede individualmente activarlas, desactivarlas, modificarlas o
añadir nuevas.</p>
</li>
<li>
<p>Las reglas del firewall generalmente <strong>se aplican</strong> solo <strong>a las cabeceras TCP/IP</strong> de las capas 3 (<strong>red</strong>) y 4 (<strong>transporte</strong>)</p>
</li>
<li>
<p>El firewall básicamente mira <strong>direcciones IP</strong> y <strong>puertos</strong>.</p>
</li>
</ul>
