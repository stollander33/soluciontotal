<h1 id="¿que-queremos-proteger" tabindex="-1"><a class="header-anchor" href="#¿que-queremos-proteger" aria-hidden="true">#</a> ¿Que queremos proteger?</h1>
<p>Protegemos recursos que forman parte del sistema:</p>
<h3 id="hardware" tabindex="-1"><a class="header-anchor" href="#hardware" aria-hidden="true">#</a> <strong>Hardware</strong></h3>
<ul>
<li>
<p>Es fundamental <strong>que no se puedan sustraer</strong>, ni el equipo entero ni alguna pieza del
mismo (=<mark>principalmente el disco duro</mark>=, pero también el dispositivo donde se hace la
copia de seguridad de ese disco).</p>
</li>
<li>
<p>En el caso de <em><strong>los portátiles</strong></em> no podemos evitar que salgan de la empresa, porque los
trabajadores visitan las dependencias del cliente o se llevan trabajo a casa. Pero sí
debemos vigilar que esos ordenadores apliquen =<mark>cifrado en el disco duro</mark>= y tengan
=<mark>contraseñas fuertes</mark>= actualizadas.</p>
</li>
<li>
<p>Es importante que <strong>no se puedan introducir nuevos equipos no autorizados</strong>. Un hacker
no necesita romper la seguridad de un servidor si puede conectar a la red de la em-
presa un equipo suyo con el software adecuado para realizar el ataque. O si puede
introducir un troyano en algún ordenador de un empleado.</p>
</li>
<li>
<p>Aplicaremos <strong>mantenimiento preventivo</strong> para evitar averías. Por ejemplo, en cada or-
denador, una vez al año, abrir la caja para limpiar los disipadores y los ventiladores,
porque el polvo acumulado puede anular su función de rebajar la temperatura del
sistema.</p>
</li>
</ul>
<h3 id="software" tabindex="-1"><a class="header-anchor" href="#software" aria-hidden="true">#</a> <strong>Software</strong></h3>
<p>Los ordenadores de una empresa deben tener las aplicaciones estrictamente necesarias
para llevar a cabo el trabajo asignado: ni más ni menos.</p>
<p>Debemos evitar instalar software extra por que puede tener vulnerabilidades que puedan dañar al sistema completo.</p>
<h3 id="datos" tabindex="-1"><a class="header-anchor" href="#datos" aria-hidden="true">#</a> <strong>Datos</strong></h3>
<p>Los datos son habitualmente el <mark>activo ‘informático’</mark> más preciado en cualquier organización</p>
<p>Como hemos dicho antes, las máquinas y las aplicaciones se compran; pero los datos
de nuestra empresa son exclusivamente suyos. Hay que protegerlos por dos aspectos:</p>
<ul>
<li>Si desaparecen, la empresa no puede funcionar con normalidad.</li>
<li>Si llegan a manos de la competencia, la estrategia empresarial y el futuro de la com-
pañía están en riesgo</li>
</ul>
<p>Las empresas modernas responden al esquema de «oficina sin papeles»: están informatizados todos los datos que entran, los generados internamente y los que comunicamos al exterior.</p>
<p>La infraestructura necesaria es amplia y compleja porque los niveles de segu-
ridad son elevados:</p>
<ul>
<li>
<p>Todos los equipos deben estar especialmente <strong>protegidos contra software malicioso</strong>
que pueda robar datos o alterarlos.</p>
</li>
<li>
<p><strong>El almacenamiento debe ser redundante</strong>: grabamos el mismo dato en más de un
dispositivo. En caso de que ocurra un fallo de hardware en cualquier dispositivo, no
hemos perdido la información.</p>
</li>
<li>
<p><strong>El almacenamiento debe ser cifrado</strong>. Las empresas manejan información muy sensible, tanto los datos personales de clientes o proveedores como sus propios informes, que pueden ser interesantes para la competencia.</p>
</li>
</ul>
<p>Si, por cualquier circunstancia, perdemos un dispositivo de almacenamiento (disco duro, pendrive USB, cinta de backup), los datos que contenga deben ser inútiles para cualquiera que no pueda descifrarlos.</p>
<h3 id="comunicaciones" tabindex="-1"><a class="header-anchor" href="#comunicaciones" aria-hidden="true">#</a> <strong>Comunicaciones</strong></h3>
<p>Los datos no suelen estar recluidos siempre en la misma máquina: en muchos casos salen
con destino a otro usuario que los necesita.</p>
<p>Esa transferencia (correo electrónico, mensajería instantánea, disco en red, servidor web) también hay que protegerla. Debemos <mark>utilizar canales cifrados</mark>, incluso aunque el fichero de datos que estamos transfiriendo ya esté cifrado (doble cifrado es doble obstáculo para el atacante).</p>
<p>Además de proteger las comunicaciones de datos, también es tarea de la seguridad informática <mark>controlar las conexiones a la red</mark> de la empresa.</p>
<p>El peligro también está en <strong>la propia oficina</strong>: no puede ser que <mark>cualquier visitante</mark> entre
en nuestra red con solo conectar su portátil a una toma de la pared o <mark>a través del wifi
de la sala de espera</mark>.</p>
