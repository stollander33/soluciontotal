<h1 id="¿que-es-seguridad" tabindex="-1"><a class="header-anchor" href="#¿que-es-seguridad" aria-hidden="true">#</a> ¿Que es seguridad?</h1>
<p>Característica que indica que un sistema esta libre de todo peligro, daño o riesgo.</p>
<div class="custom-container tip"><p class="custom-container-title">FIABILIDAD:</p>
<p>Probabilidad de que un sistema se comporte tal y como se espera de el</p>
<ul>
<li>
<p><strong>Confidencialidad</strong>: Los objetos sólo pueden ser accedidos por actores autorizados y de una forma controlada.</p>
</li>
<li>
<p><strong>Integridad</strong>: Los objetos sólo pueden ser modificados por actores autorizados.</p>
</li>
<li>
<p><strong>Disponibilidad</strong>: Los objetos han de permanecer accesibles a los actores autorizados.</p>
</li>
<li>
<p><strong>No repudio</strong>: Un actor no puede negar lo que ha dicho.</p>
</li>
</ul>
